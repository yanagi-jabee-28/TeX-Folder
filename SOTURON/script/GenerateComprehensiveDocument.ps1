## 第Ⅱ部：測定原理と方法論

### 4. 複素誘電率測定法の分類と特徴

#### 4.1 測定法の分類体系

複素誘電率の測定法は、以下の3つに大別される：

1. **集中定数法（100Hz〜1GHz）**
2. **反射伝送法（1GHz〜100GHz以上）**
3. **共振法（1GHz〜100GHz、低損失材料）**

#### 4.2 集中定数法

**原理:**
- 試料を電極で挟んでコンデンサを形成
- インピーダンスアナライザでC（キャパシタンス）とG（コンダクタンス）を測定

**誘電率の導出:**

\$\$
\\epsilon_r' = \\frac{tC}{A\\epsilon_0}, \\quad \\epsilon_r'' = \\frac{tG}{\\omega A \\epsilon_0}
\$\$

ここで、t: 試料厚さ、A: 電極面積、\$\\omega\$: 角周波数

**適用範囲:**
- 周波数: 〜1GHz程度
- 集中定数近似が成立する条件（回路サイズ << 波長）

#### 4.3 反射伝送法（Sパラメータ法）

**原理:**
- 試料を伝送線路に挿入
- VNAでSパラメータ（反射・透過特性）を測定
- 逆問題を解いて複素誘電率を導出

**主な手法:**
- **同軸Sパラメータ法:** 同軸線路に試料を装填
- **フリースペース法:** 空間伝播を利用、非破壊測定

**適用範囲:**
- 高損失材料（tanδ > 0.01程度）
- 広帯域測定（数GHz〜100GHz以上）

**課題:**
- 低損失材料では測定精度が低下
- ポートマッチング特性の影響

#### 4.4 共振法

**原理:**
- 共振器内に試料を配置
- 共振周波数 \$f_r\$ とQ値を測定
- 試料有無での変化から複素誘電率を導出

**主な手法:**
- **BCDR（平衡円板共振器法）:** 垂直方向測定
- **SPDR（分割円柱共振器法）:** 水平方向測定
- **空洞共振器法:** 高精度、単一周波数

**優位性:**
- 低損失材料（tanδ < 0.01）の高感度測定
- 共振によるエネルギー蓄積でS/N比向上
- tanδの測定精度が極めて高い

**制約:**
- 基本的に単一周波数測定（高次モードで複数点可能）
- 試料形状に制約

### 5. 平衡円板共振器法（BCDR）の原理

#### 5.1 BCDR の構造と測定原理

**基本構造:**
- 上下を導体電極で挟んだ円筒共振器
- 中央に円板状の銅箔電極
- 2枚の誘電体シート（試料）を対称配置
- 同軸線路で励振

**測定原理:**
1. VNAから高周波信号を入力
2. TM_{0m0}モードの共振を励起
3. S_{21}パラメータから共振特性を測定
4. 共振周波数 → \$\\epsilon_r'\$
5. Q値 → tanδ（\$\\epsilon_r''\$）

#### 5.2 TM_{0m0}モードの特性

**電磁界分布:**
- 電界: 垂直成分（E_z）のみ
- 磁界: 円周方向成分（H_φ）のみ
- 円筒対称

**モード表記:**
- TM: Transverse Magnetic（伝搬方向に対して磁界が横）
- 0: 円周方向（φ）に変動なし
- m: 半径方向にm個の腹
- 0: 高さ方向（z）に変動なし

**測定上の利点:**
- 電界が基板に垂直→垂直方向の\$\\epsilon_r\$ を直接測定
- 簡素な電磁界分布→理論解析が容易
- 不要モードの励振を抑制しやすい

#### 5.3 共振周波数と誘電率の関係

TM_{0m0}モードの共振周波数は、ベッセル関数を含む超越方程式で表される：

\$\$
f_{0m0} = \\frac{c}{2\\pi R \\sqrt{\\epsilon_r}} x'_{0m0}
\$\$

ここで、
- c: 光速
- R: 円板電極の半径
- \$\\epsilon_r\$: 比誘電率
- \$x'_{0m0}\$: ベッセル関数の零点

**実用的な算出:**
- 測定した\$f_r\$ と既知のR, dから数値的に\$\\epsilon_r\$ を求める
- 高次モード（m=1,2,3,...）で複数周波数測定可能

#### 5.4 Q値と誘電正接の関係

**Q値の定義（共振器）:**

\$\$
Q = \\omega_r \\frac{W_{stored}}{P_{loss}} = \\frac{f_r}{\\Delta f_{3dB}}
\$\$

**無負荷Q（Q_0）の導出:**

\$\$
Q_0 = \\frac{Q_L}{1 - 10^{IL_r / 20}}
\$\$

ここで、
- \$Q_L\$: 負荷Q（測定値）
- \$IL_r\$: 共振ピークでの挿入損失（dB）

**損失分離:**

\$\$
\\frac{1}{Q_0} = \\frac{1}{Q_c} + \\frac{1}{Q_d}
\$\$

- \$Q_c\$: 導体損失によるQ（理論計算）
- \$Q_d\$: 誘電損失によるQ → tanδ

**誘電正接の決定:**

\$\$
\\tan\\delta = \\frac{1}{Q_d} = \\frac{1}{Q_0} - \\frac{1}{Q_c}
\$\$

### 6. フリースペース法と広帯域測定

#### 6.1 フリースペース法の原理

**測定構成:**
- ホーンアンテナから平面波を照射
- 試料を透過（または反射）した波をアンテナで受信
- VNAでSパラメータを測定

**特徴:**
- 非接触測定
- 広帯域測定に適する（数GHz〜100GHz以上）
- 試料の水平方向（面内方向）を測定

#### 6.2 試料サイズの制約

電波の照射面積は波長の約3倍必要：

- 低周波（例：26.5GHz）→ 大きな試料が必要
- 高周波（例：90GHz）→ 小さな試料でも測定可能

**サンプルホルダーの適用限界:**
- Φ31mm → 50GHz以下で測定困難
- Φ51mm → 30GHz以下で測定困難

#### 6.3 BCDRとフリースペースの組み合わせ

**相補的な特性:**
- BCDR: 60GHz以下で高精度（低損失材料）
- フリースペース: 40GHz以上で安定測定

**広帯域測定戦略:**
- 低周波側: BCDRで高精度測定
- 高周波側: フリースペースで測定
- 重なる帯域で測定結果を相互検証

### 7. Sパラメータとベクトルネットワークアナライザ

#### 7.1 Sパラメータの定義と物理的意味

2ポート素子の場合、Sパラメータは行列で表される：

\$\$
\\begin{pmatrix} b_1 \\\\ b_2 \\end{pmatrix} = \\begin{pmatrix} S_{11} & S_{12} \\\\ S_{21} & S_{22} \\end{pmatrix} \\begin{pmatrix} a_1 \\\\ a_2 \\end{pmatrix}
\$\$

**各パラメータの意味:**
- \$S_{11}\$: ポート1での反射特性（入力反射係数）
- \$S_{21}\$: ポート1→ポート2への透過特性（順方向伝送）
- \$S_{12}\$: ポート2→ポート1への透過特性（逆方向伝送）
- \$S_{22}\$: ポート2での反射特性（出力反射係数）

#### 7.2 VNAの測定原理（ヘテロダイン検波）

**基本動作:**
1. 信号源から高周波信号を発生
2. 方向性結合器で基準信号（a）と測定信号（b）を分離
3. 局部発振器（LO）と混合してIF（中間周波数）に変換
4. IFで振幅と位相を精密測定

**ダウンコンバートの利点:**
- GHz帯の信号を直接測定する困難さを回避
- IF帯（数MHz）で高精度な位相測定が可能
- A/D変換とデジタル信号処理が容易

#### 7.3 校正の必要性と方法

**校正の目的:**
- ケーブル、コネクタ、アダプタの影響除去
- 測定系の系統誤差を補正

**主な校正法:**
1. **SOLT校正（Short-Open-Load-Thru）**
   - 最も一般的
   - 校正キットが必要
   - 同軸系で広く使用

2. **TRL校正（Thru-Reflect-Line）**
   - マイクロストリップライン等に適用
   - 高精度校正が可能
   - 特殊な校正基板が必要

**校正後の注意:**
- ケーブルの曲げを変えない
