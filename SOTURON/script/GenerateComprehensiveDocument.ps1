- 算出値と実測値が良好に一致
- L6504C2を除き基板間の差は小さい（約2dB）
- 減衰量の差は1.2倍程度、実用上は許容範囲

**考察:**
- FR-4基板間の\$\\epsilon_r'\$, \$\\epsilon_r''\$ の差は、伝送特性に大きな影響を与えない
- 設計時には材料選定よりも他の要因（線路設計等）が重要
