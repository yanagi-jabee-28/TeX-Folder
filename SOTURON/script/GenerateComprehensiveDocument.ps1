## 第Ⅳ部：データ解析と結果解釈

### 12. 測定結果の解析手法

#### 12.1 共振ピークの自動検出

**BCDRソフトウェアの機能:**
- S_{21}全体掃引からTM_{0m0}モードを自動識別
- 理論共振周波数との照合
- 不要モードの除外

**手動編集:**
- 誤検出されたピークの削除
- 検出漏れピークの追加
- 各ピークの周波数範囲調整

#### 12.2 負荷Q（Q_L）と無負荷Q（Q_0）の算出

**負荷Qの測定:**

\$\$
Q_L = \\frac{f_r}{\\Delta f_{3dB}}
\$\$

- マーカーで共振周波数\$f_r\$ を読取
- ピーク頂点から-3dB下がる点の周波数幅\$\\Delta f\$ を測定

**無負荷Qへの変換:**

\$\$
Q_0 = \\frac{Q_L}{1 - 10^{IL_r / 20}}
\$\$

- \$IL_r\$: 共振ピークでの挿入損失（dB、負値）

**物理的意味:**
- \$Q_L\$: VNA結合を含む系全体のQ
- \$Q_0\$: 共振器自身の真のQ

#### 12.3 比誘電率の決定

**手順:**
1. 各TM_{0m0}モードの共振周波数\$f_r\$ を取得
2. BCDR寸法（R, d, 励振孔径等）を入力
3. 理論式（ベッセル関数方程式）を数値的に解く
4. \$\\epsilon_r'\$ を決定

**複数モードの利用:**
- m=1, 2, 3, ... の複数共振点で\$\\epsilon_r'\$ を算出
- 周波数特性の把握
- 測定の信頼性確認（値の一致）

#### 12.4 誘電正接の決定

**損失分離の実行:**

\$\$
\\frac{1}{Q_d} = \\frac{1}{Q_0} - \\frac{1}{Q_c}
\$\$

**導体Qの計算:**
- 銅の導電率σ（固定値または周波数特性）を入力
- 表皮深さδ_s = 1/√(πfμσ)
- 電磁界理論により\$Q_c\$ を計算

**誘電正接の導出:**

\$\$
\\tan\\delta = \\frac{1}{Q_d}
\$\$

**周波数特性のプロット:**
- 横軸: 周波数（GHz）
- 縦軸: tanδ
- 複数モードのデータを表示
- トレンド確認と異常値除外

### 13. 損失分離の方法論

#### 13.1 全損失と成分損失の関係

**伝送線路における減衰:**

\$\$
\\alpha = \\alpha_c + \\alpha_d
\$\$

- \$\\alpha_c\$: 導体損失による減衰
- \$\\alpha_d\$: 誘電損失による減衰

**共振器におけるQ値:**

\$\$
\\frac{1}{Q_0} = \\frac{1}{Q_c} + \\frac{1}{Q_d}
\$\$

**減衰とQ値の関係:**

\$\$
\\alpha = \\frac{\\beta}{2Q} = \\frac{\\omega}{2Q v_g}
\$\$

ここで、
- β: 位相定数
- \$v_g\$: 群速度
- 低分散媒質では \$v_g ≈ v_p = ω/β\$

#### 13.2 導体損失の理論計算

**表皮効果:**

\$\$
\\delta_s = \\sqrt{\\frac{2}{\\omega\\mu\\sigma}} = \\frac{1}{\\sqrt{\\pi f \\mu \\sigma}}
\$\$

**表面抵抗:**

\$\$
R_s = \\frac{1}{\\sigma\\delta_s} = \\sqrt{\\frac{\\pi f \\mu}{\\sigma}}
\$\$

**減衰定数（マイクロストリップライン）:**

\$\$
\\alpha_c = \\frac{R_s}{Z_0 W}
\$\$

ここで、
- \$Z_0\$: 特性インピーダンス
- W: 線路幅

#### 13.3 誘電損失の計算

**誘電損失による減衰:**

\$\$
\\alpha_d = \\frac{\\pi f}{c} \\cdot \\epsilon_r'' \\frac{\\sqrt{\\epsilon_{re}} (\\epsilon_{re} - 1)}{\\epsilon_{re} (\\epsilon_r' - 1)}
\$\$

ここで、
- \$\\epsilon_{re}\$: 実効誘電率
- c: 光速

**誘電正接との関係:**

\$\$
\\epsilon_r'' = \\epsilon_r' \\tan\\delta
\$\$

#### 13.4 表面粗さの影響

**粗さモデル:**
- Hammerstad-Groisse モデル
- Huray snowball モデル

**主要メカニズム:**
1. 電流経路の増大（トポロジー効果）
2. 局所的磁界集中（近接効果）

**実効的な取り扱い:**
- 表面粗さを考慮した実効導電率σ_eff
- σ_eff < σ_DC（高周波ほど顕著）

### 14. 測定不確かさと誤差要因

#### 14.1 BCDRにおける主要誤差要因

**幾何パラメータ:**
| パラメータ | 影響 | 管理方法 |
|:-----------|:-----|:---------|
| 円板電極直径D | \$\\epsilon_r'\$ に直接影響 | 顕微鏡測定、±0.01mm |
| 円板電極厚さt_c | エッジ効果補正 | ソフトウェアで自動補正 |
| 試料厚さt | tanδに大きく影響 | マイクロメータ、±1μm |
| 励振孔径 | 共振周波数計算 | 検査報告書、定期再測定 |

**電気パラメータ:**
- 導電率σ: tanδ計算に直接影響、周波数特性を使用
- Q値測定精度: IFBW、カップリング調整

**試料関連:**
- エアギャップ: トルク管理、周波数モニタ
- 試料の不均一性: 複数試料で確認
- 表面粗さ: Shimシート損傷防止

#### 14.2 円板電極の位置ズレ

**許容誤差:**
- 100μm以下: 影響軽微
- 100μm以上: 不要モード増大、tanδばらつき

**位置合わせ手法:**
- Shimシート（ドーナツ）による機械的ガイド
- 吸着ピンセットで慎重に配置
- 不要モードの大きさで評価

**不要モード対策:**
- 円板位置の微調整
- 電極サイズ変更（Φ15mm ↔ Φ18mm）
- 試料厚さ変更で回避

#### 14.3 上下試料の不平衡

**要求:**
- 比誘電率: ±0.2%以内
- 厚さ: ±1%以内（\$\\epsilon_r\$ < 6の場合）

**不平衡の影響:**
- tanδの悪化（平衡が崩れる）
- \$\\epsilon_r'\$ への影響は軽微

**対策:**
- 同一ロット・同一基板から試料作成
- 厚さの個別測定とペアリング

#### 14.4 電極表面の経年劣化

**劣化のメカニズム:**
- 酸化
- 誘電体成分の付着
- 微小キズ

**影響:**
- 実効導電率σ_eff の低下
- tanδの見かけ上の増大

**対策:**
1. 定期的なPTFE標準試料での校正確認
2. 導電率周波数特性の補正
3. 定期的な電極研磨（2〜3年周期）

