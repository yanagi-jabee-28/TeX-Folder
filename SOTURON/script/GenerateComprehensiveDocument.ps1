### 10. 校正手順と測定精度の確保

#### 10.1 2ポートフル校正の実施

**校正の理由:**
- BCDRはマッチング特性が悪い
- 多重反射の影響が大きい
- 特に低損失材料で影響顕著

**校正手順（SOLT法の例）:**
1. VNAとBCDRを同軸ケーブルで接続
2. ケーブルをBCDRから外す
3. ケーブル先端で校正キットを使用
   - Short（短絡）
   - Open（開放）
   - Load（50Ω整合負荷）
   - Thru（Port1とPort2を直結）
4. 校正完了後、ケーブルをBCDRに再接続
5. 校正データを保存（.csa）

**校正後の確認:**
- 共振ピークにリップルが少ないこと
- ピーク形状が左右対称であること
- ピークレベルが適切（-30dB〜-35dB）

#### 10.2 カップリング調整

**調整の目的:**
- 共振ピークレベルを最適範囲に設定
- S/N比の確保とピーク歪みの抑制

**最適レベル:**
- 推奨: -30dB〜-35dB
- 高すぎる（-20dB等）: ピーク歪み、tanδ過大評価
- 低すぎる（-60dB等）: S/N低下、tanδばらつき増大

**調整方法:**
1. 上下の調整ノブを回転（最大4回転＝2mm移動）
2. VNAでS_{21}をモニタしながら調整
3. 上下のカップリングを同程度に
4. 調整は慎重に（励振部は精密構造）

#### 10.3 エアギャップ管理

**エアギャップの影響:**
- 見かけ上の誘電率が低下
- tanδが低く見積もられる
- 特に高誘電率・低損失材料で顕著

**最小化の方法:**
1. クランプで均一に加圧
2. トルクレンチでトルク管理（例：2N·m）
3. 共振周波数をモニタ
   - 締め付け → 周波数低下
   - 変化が止まるまで締める
4. 柔らかい試料：適度なトルクで停止

**最適トルクの判定:**
- 共振周波数の周波数-トルク曲線
- 曲線部→リニア部の変曲点を外挿
- 試料表面に円板の痕が明瞭に残る場合は過大

### 11. 導電率測定の理論と手順

#### 11.1 導電率測定の原理

**基本概念:**
- 標準Cu円板（既知導電率σ₁）で誘電体を測定 → Q₀, tanδ既知
- 測定対象金属円板（未知導電率σ₂）で同じ誘電体を測定 → Q₀'変化
- Q値の変化から導電率σ₂を算出

**理論式:**

\$\$
\\frac{1}{Q_0} = \\frac{1}{Q_c} + \\frac{1}{Q_d}
\$\$

\$Q_c\$ は導電率σの関数として理論計算可能

#### 11.2 バルク金属の導電率測定（ケース1, 2）

**試料構成:**
- 誘電体: PTFE等の低損失材料（tanδ既知）
- 円板電極: 異種金属（SUS等）または粗化Cu

**手順:**
1. **STEP1:** 標準Cu円板でPTFEのtanδを測定
2. **STEP2:** 測定対象金属円板に交換して再測定
3. 両測定のQ値差から導電率を計算

**適用例:**
- 異種金属（SUS, Al等）の高周波導電率
- 表面粗化の影響評価

#### 11.3 界面導電率測定（ケース3, 4）

**測定対象:**
- 銅張基板の銅箔表面導電率
- 接着界面の実効導電率

**試料準備:**
- 銅張基板から銅を剥離した誘電体基板
- 銅円板付き基板（エッチングで作成）

**手順（ケース3）:**
1. **STEP1:** 標準Cu円板で誘電体のtanδ測定
2. **STEP2:** 上側は誘電体、下側は銅円板付き基板で測定
3. Q値差から界面導電率を算出

**手順（ケース4・推奨）:**
1. **STEP1:** 標準Cu円板で誘電体のtanδ測定
2. **STEP2:** 銅円板付き基板2枚を背中合わせに設置
3. 両面の界面導電率を同時評価

**精度向上のコツ:**
- 銅円板の位置合わせが極めて重要
- 不要モードの発生を最小化
- 複数試料で再現性確認

#### 11.4 導電率測定の制約条件

**試料条件:**
- 比誘電率: 1.1〜10
- 誘電正接: < 2×10⁻³（重要）
- 基板厚さ: 0.2mm〜0.4mm推奨

**円板電極:**
- 直径: 標準Cu電極と±0.1mm以内
- 厚さ: 0.01mm〜0.1mm
  - 0.06mm以下ならShim除去推奨

**導電率測定範囲:**
- 上限: 〜5×10⁷ S/m（tanδ依存）
- 下限: 1×10⁶ S/m程度

**測定不確かさ:**
- tanδ = 1×10⁻⁴の場合: σ < 4.4×10⁷ S/mで15%以下
- tanδ = 1×10⁻³の場合: σ < 2.8×10⁷ S/mで15%以下

