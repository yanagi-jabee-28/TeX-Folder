### 付録A: 重要な数式一覧

#### A.1 複素誘電率関連

**複素誘電率:**
\$\$ \\dot{\\epsilon} = \\epsilon' - j\\epsilon'' = \\epsilon_r' \\epsilon_0 - j\\epsilon_r'' \\epsilon_0 \$\$

**誘電正接:**
\$\$ \\tan\\delta = \\frac{\\epsilon_r''}{\\epsilon_r'} \$\$

**集中定数法:**
\$\$ \\epsilon_r' = \\frac{tC}{A\\epsilon_0}, \\quad \\epsilon_r'' = \\frac{tG}{\\omega A \\epsilon_0} \$\$

#### A.2 共振器関連

**Q値定義（エネルギー）:**
\$\$ Q = \\omega_r \\frac{W_{stored}}{P_{loss}} \$\$

**Q値定義（周波数）:**
\$\$ Q_L = \\frac{f_r}{\\Delta f_{3dB}} \$\$

**無負荷Qへの変換:**
\$\$ Q_0 = \\frac{Q_L}{1 - 10^{IL_r / 20}} \$\$

**損失分離:**
\$\$ \\frac{1}{Q_0} = \\frac{1}{Q_c} + \\frac{1}{Q_d} \$\$

**誘電正接:**
\$\$ \\tan\\delta = \\frac{1}{Q_d} = \\frac{1}{Q_0} - \\frac{1}{Q_c} \$\$

#### A.3 伝送線路関連

**減衰の成分:**
\$\$ \\alpha = \\alpha_c + \\alpha_d \$\$

**減衰とQ値:**
\$\$ \\alpha = \\frac{\\beta}{2Q} = \\frac{\\omega}{2Q v_g} \$\$

**表皮深さ:**
\$\$ \\delta_s = \\sqrt{\\frac{2}{\\omega\\mu\\sigma}} = \\frac{1}{\\sqrt{\\pi f \\mu \\sigma}} \$\$

**表面抵抗:**
\$\$ R_s = \\frac{1}{\\sigma\\delta_s} = \\sqrt{\\frac{\\pi f \\mu}{\\sigma}} \$\$

**導体損失減衰:**
\$\$ \\alpha_c = 0.8686 \\times \\frac{R_s}{Z_0 W} = 0.8686 \\times \\frac{\\sqrt{\\pi f \\mu_0 \\rho}}{Z_0 W} \\ [\\text{dB/100mm}] \$\$

**誘電損失減衰:**
\$\$ \\alpha_d = 0.8686 \\times \\frac{f\\pi}{c} \\cdot \\epsilon_r'' \\frac{\\sqrt{\\epsilon_{re}} (\\epsilon_{re} - 1)}{\\epsilon_{re} (\\epsilon_r' - 1)} \\ [\\text{dB/100mm}] \$\$

**特性インピーダンス（マイクロストリップ）:**
\$\$ Z_0 = \\frac{87}{\\sqrt{\\epsilon_r' + 1.41}} \\ln \\left( \\frac{5.98H}{0.8W + T} \\right) \$\$

**実効誘電率:**
\$\$ \\epsilon_{re} = \\frac{\\epsilon_r' + 1}{2} + \\frac{\\epsilon_r' - 1}{2} \\left( 1 + \\frac{10H}{W} \\right)^{-1/2} \$\$

### 付録B: 測定トラブルシューティング

#### B.1 共振ピークが検出されない

**原因と対策:**
1. **カップリングが弱すぎる**
   - 調整ノブで励振線を電極に近づける
   - S_{21}をモニタしながら調整

2. **試料が導電性**
   - 銅箔が残っていないか確認
   - 絶縁抵抗を測定

3. **周波数範囲が適切でない**
   - 理論共振周波数を計算
   - 掃引範囲を拡大

4. **校正が無効**
   - 再校正を実施

#### B.2 共振ピークが歪んでいる

**原因と対策:**
1. **円板電極の位置ズレ**
   - 不要モードが大きい
   - 円板位置を微調整
   - Shimシート使用を確認

2. **カップリングが強すぎる**
   - ピークレベルが高い（-20dB等）
   - 調整ノブで励振線を遠ざける

3. **エアギャップ**
   - トルク不足
   - クランプを締め直し

4. **校正不良**
   - リップルが多い
   - 再校正

#### B.3 tanδが異常に高い/低い

**高い場合の原因:**
1. エアギャップ
2. 試料の損傷・汚れ
3. 不要モードの影響
4. 導電率設定誤り（高すぎる）

**低い場合の原因:**
1. 導電率設定誤り（低すぎる）
2. 測定系の問題
3. 試料の異常（異物混入等）

**対策:**
- 導電率設定の確認・再設定
- 試料の再準備
- 円板位置の再調整
- PTFE標準試料で検証

#### B.4 再現性が悪い

**原因と対策:**
1. **円板位置のバラつき**
   - Shimシート使用
   - 設置手順の見直し

2. **エアギャップのバラつき**
   - トルク管理の徹底
   - 締付トルク記録

3. **温度変化**
   - 室温安定化
   - 校正と測定の時間間隔短縮

4. **VNA drift**
   - 再校正頻度を上げる

### 付録C: 用語集

**BCDR（Balanced Circular Disk Resonator）**  
平衡円板共振器法。円板状試料を上下対称に配置した共振器により、垂直方向の複素誘電率を測定する手法。

**VNA（Vector Network Analyzer）**  
ベクトルネットワークアナライザ。Sパラメータの振幅と位相を測定する装置。

**Sパラメータ（Scattering Parameters）**  
高周波回路の反射・透過特性を表す散乱行列。S_{11}: 入力反射、S_{21}: 順方向透過、S_{12}: 逆方向透過、S_{22}: 出力反射。

**TM_{0m0}モード**  
円筒共振器の電磁界モード。電界が軸方向、磁界が円周方向の円筒対称モード。0: 円周方向変動なし、m: 半径方向の腹の数、0: 軸方向変動なし。

**Q値（Quality Factor）**  
共振器のエネルギー蓄積効率。Q = ω(蓄積エネルギー)/(損失電力) = f_r/Δf_{3dB}。高いほど低損失。

**誘電正接（Loss Tangent, tanδ）**  
誘電損失と誘電率の比。tanδ = ε_r''/ε_r'。材料の損失特性を表す指標。

**表皮効果（Skin Effect）**  
高周波電流が導体表面に集中する現象。表皮深さδ_s = 1/√(πfμσ)。

**IFBW（Intermediate Frequency BandWidth）**  
VNAの中間周波数帯域幅。狭いほどS/N向上、測定時間増。

**校正（Calibration）**  
測定系の系統誤差を補正する手順。SOLT校正、TRL校正等。

**AFR（Automatic Fixture Removal）**  
自動フィクスチャ除去。測定対象以外の伝送特性を除去する機能。

**フリースペース法（Free Space Method）**  
空間伝播を利用した非接触の複素誘電率測定法。水平方向を測定。

**SPDR（Split Post Dielectric Resonator）**  
分割円柱共振器法。水平方向の複素誘電率を測定。

**FR-4（Flame Retardant-4）**  
ガラスエポキシ基板。最も一般的なプリント基板材料。

**MEGTRON6/7**  
Panasonic製の超低損失基板材料。20GHz以上の高周波対応。

