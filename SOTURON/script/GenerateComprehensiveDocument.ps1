- L6504C2のみ顕著な減衰（約2dB大）
- 他の4基板は類似特性

#### 16.2 減衰量の理論計算

**減衰の成分:**

\$\$
\\alpha = \\alpha_c + \\alpha_d
\$\$

**導体損失:**

\$\$
\\alpha_c = 0.8686 \\times \\frac{\\sqrt{\\pi f \\mu_0 \\rho}}{Z_0 W} \\ [\\text{dB/100mm}]
\$\$

**誘電損失:**

\$\$
\\alpha_d = 0.8686 \\times \\frac{f\\pi}{c} \\cdot \\epsilon_r'' \\frac{\\sqrt{\\epsilon_{re}} (\\epsilon_{re} - 1)}{\\epsilon_{re} (\\epsilon_r' - 1)} \\ [\\text{dB/100mm}]
\$\$

**周波数依存性:**
- \$\\alpha_c \\propto \\sqrt{f}\$（表皮効果）
- \$\\alpha_d \\propto f\$（誘電損失）
- 高周波では\$\\alpha_d\$ が支配的

#### 16.3 実測と計算の比較

**北原卒論の結果:**
- BCDR測定の\$\\epsilon_r'\$, \$\\epsilon_r''\$ を使用
- マイクロメータ測定の基板厚H、線路幅Wを使用
- 式(5-3), (5-4)により\$\\alpha_c\$, \$\\alpha_d\$ を計算
- \$S_{dd21} = -\\alpha = -(\\alpha_c + \\alpha_d)\$ を算出

