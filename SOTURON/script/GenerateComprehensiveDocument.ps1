## 第Ⅰ部：理論的基礎

### 1. 研究背景と高周波通信の課題

#### 1.1 データ通信の高速化とGHz帯への移行

近年、スマートフォンや4Kテレビなどで扱うデータ量が飛躍的に増加しており、電子機器内の伝送速度はGHz帯へと高速化している。将来的には400Gbpsの高速通信も想定されており、これらの信号伝送には高周波特性に優れた基板材料が不可欠である。

**主要課題:**
- 周波数依存性による複素誘電率の変化
- 誘電損失による信号減衰
- 特性インピーダンスの不整合による反射
- 基板材料ごとの特性差異の把握

#### 1.2 FR-4基板の課題と低損失基板の必要性

汎用基板として広く用いられるFR-4（ガラスエポキシ）は、コストと加工性に優れる一方、GHz帯では以下の問題が生じる：

- 誘電正接（tanδ）が比較的大きく、高周波で損失が増大
- 周波数による誘電率変化が生じる
- 異方性（垂直方向と水平方向で特性が異なる）が存在

これに対し、MEGTRON6/7のような低損失基板は：
- tanδが0.002程度と極めて低い
- 20GHz以上での使用を想定した設計
- 周波数特性が安定

#### 1.3 測定技術の重要性

基板材料を適切に評価するには、GHz〜100GHz帯域における複素誘電率の高精度測定が不可欠である。特に：

- 低損失材料（tanδ < 0.01）の測定には共振器法が必須
- 広帯域測定により周波数特性を把握
- 測定不確かさの定量化と管理

### 2. 誘電体の物理と電気的特性

#### 2.1 誘電体の定義と分極メカニズム

**誘電体とは:**  
外部電界が印加されたときにエネルギーを蓄積できる材料。導体のように電流が流れず、絶縁体として機能しながら、分極によって電界に応答する。

**分極の種類:**

1. **電子分極**  
   - 原子核周りの電子雲が変位
   - 最も高速（紫外線領域まで追随）
   - すべての誘電体に存在

2. **イオン分極**  
   - イオン結晶で陽イオンと陰イオンが逆向きに変位
   - 赤外線領域程度まで追随
   - セラミック材料などに顕著

3. **双極子分極（配向分極）**  
   - 永久双極子を持つ分子が電界方向に配向
   - マイクロ波領域程度まで追随
   - FR-4などの高分子材料の主要メカニズム

#### 2.2 複素誘電率の数学的表現

時間的に変化する電界 \$E(t) = E_0 e^{j\\omega t}\$ に対し、分極の応答に遅れ \$\\delta\$ が生じる場合、誘電率は複素数で表される：

\$\$
\\dot{\\epsilon} = \\epsilon' - j\\epsilon'' = \\epsilon_r' \\epsilon_0 - j\\epsilon_r'' \\epsilon_0
\$\$

**物理的意味:**
- **実部 \$\\epsilon_r'\$（比誘電率）:** エネルギーの蓄積能力
- **虚部 \$\\epsilon_r''\$（誘電損失）:** エネルギーの散逸（熱への変換）

#### 2.3 誘電正接（tanδ）

誘電正接は、材料の損失特性を表す最も重要な指標：

\$\$
\\tan\\delta = \\frac{\\epsilon_r''}{\\epsilon_r'}
\$\$

**物理的解釈:**
- 蓄積エネルギーに対する損失エネルギーの比率
- 理想コンデンサからの位相ズレ角度のタンジェント
- 値が小さいほど低損失材料

**典型値:**
- FR-4: 0.02程度（高周波）
- MEGTRON6: 0.002程度（@ 40GHz）
- MEGTRON7: 0.001以下（@ 14GHz）
- PTFE（テフロン）: 0.0002程度

#### 2.4 誘電分散と周波数特性

周波数が高くなると、分極が電界変化に追随できなくなる現象を誘電分散という。

**分散の特徴:**
- 双極子分極：〜GHz帯で追随限界
- イオン分極：〜THz帯（赤外線）で追随限界
- 電子分極：〜PHz帯（紫外線）で追随限界

**実用上の影響:**
- FR-4では数十GHz以上で\$\\epsilon_r'\$ が減少傾向
- MEGTRON6/7は測定範囲で\$\\epsilon_r'\$ が安定
- \$\\epsilon_r''\$ は一般に周波数とともに増加傾向

### 3. 基板材料の特性と異方性

#### 3.1 一般的なプリント基板材料の比較

| 基板材料 | 比誘電率 \$\\epsilon_r'\$ | 誘電正接 tanδ | 特徴・用途 |
|:---------|:------------------------:|:-------------:|:-----------|
| ガラスエポキシ（FR-4） | 4.0〜5.2 | 0.02程度 | 最も一般的、安価、強度高い |
| MEGTRON6（Panasonic） | 3.6程度 | 0.002 | 超低損失、20GHz以上対応 |
| MEGTRON7（Panasonic） | 3.31程度 | < 0.001 | 極低損失、最先端通信用 |
| テフロン（PTFE） | 2.1 | 0.0002 | GHz帯で安定、高価 |
| セラミック | 4.5〜5.2 | < 0.001 | 高周波安定、放熱性良好、高価 |
| フェノール（紙基板） | 4.6〜5.5 | 大 | 最安価、電気特性劣る |

#### 3.2 FR-4基板の構造と異方性

**FR-4の構造:**
- ガラスクロス（織布）を多層に重ねる
- エポキシ樹脂を含浸させ熱硬化
- ガラス繊維とエポキシ樹脂の複合材料

**異方性の発生原因:**
- ガラス繊維が特定方向に配向（織布構造）
- 垂直方向（厚さ方向）と水平方向で構造が異なる
- \$\\epsilon_r'\$ と \$\\epsilon_r''\$ の両方に異方性

**測定上の重要性:**
- 垂直方向：BCDR法で測定（TM_{0m0}モード）
- 水平方向：SPDR法やフリースペース法で測定
- 異方性を考慮しないと設計誤差が生じる

